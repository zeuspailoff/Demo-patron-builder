package Builder;

public class vehiculo {
    public static String marca;
    public String tipo;
    public String motor;
    public int puertas;

    public String getMarca() {
        return marca;
    }




    public String getTipo() {
        return tipo;
    }



    public String getMotor() {
        return motor;
    }



    public int getPuertas() {
        return puertas;
    }


}
